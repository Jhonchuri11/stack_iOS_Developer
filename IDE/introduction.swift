- Ha notado que tener estos procesos separados no es muy eficiente 
y en su lugar decide reunirlos todos bajo un mismo techo.


DEFINICIÓN:

Al igual que una fábrica de automóviles todo en uno, un entorno de 
desarrollo integrado (IDE) administra y alberga todas las tareas
necesarias para crear software. Es un conjunto de herramientas de 
desarrollo que permite a los desarrolladores escribir y crear programas
de manera eficaz. Los IDE vienen con interfaces gráficas de usuario 
(GUI) que facilitan su navegación y uso.


Un IDE consta de múltiples componentes. Uno de ellos es el editor de 
código en el que podemos escribir código. Los editores de código son 
similares a los editores de texto, pero pueden venir con características 
adicionales como el resaltado de sintaxis .

*/