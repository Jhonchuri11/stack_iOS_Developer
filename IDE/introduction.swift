/*

Imagina que eres dueño de una empresa de automóviles. 
Tienes fábricas que fabrican diferentes componentes de cada automóvil:

- Una línea de montaje que construye cada pieza.
- Un taller de pintura que pinta la carrocería.
- Un servicio de camiones que lleva los coches fabricados 
