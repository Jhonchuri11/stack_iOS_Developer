---- QUÉ ES iOS:

iOS es el segundo sistema operativo más 
instalado del mundo. Una de las principales razones 
por las que la gente elige iOS es por su sólido conjunto de 
funciones de privacidad, muchas de las cuales superan a las 
de sus rivales en el ámbito de los sistemas operativos para
smartphones. iOS incluye un sandbox que aísla los programas, 
algo que no hace ninguno de sus competidores, lo que dificulta 
que los actores maliciosos accedan a la información.


---- DESARROLLO DE iOS con SWIFT

Swift y Objective-C son los dos principales lenguajes de programación
que ofrece Apple para crear aplicaciones iOS. Objective-C era el 
lenguaje principal utilizado para el desarrollo de iOS antes del 
lanzamiento de Swift. En 2014, Apple presentó Swift como la alternativa 
más simple y moderna a Objective-C en el mundo del desarrollo de iOS.

